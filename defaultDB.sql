-- Create Tables
CREATE TABLE HOSPITAL(
     Hos_id VARCHAR(15) NOT NULL,
     Hos_name VARCHAR(40) NOT NULL,
     Hos_location VARCHAR(200) NOT NULL,
     PRIMARY KEY(Hos_id)
  );

CREATE TABLE PATIENT(
   Pat_id VARCHAR(15) NOT NULL,
   Pat_name VARCHAR(15) NOT NULL,
   Pat_password VARCHAR(15) NOT NULL,
   Pat_address VARCHAR(100),
   Sex VARCHAR(5),
   Blood_type VARCHAR(5),
   PRIMARY KEY(Pat_id)
 );

CREATE TABLE RESERVATION(
    Res_id    VARCHAR2(15) NOT NULL,
    Symptom VARCHAR2(200),
    State   NUMBER(3)  DEFAULT 0 NOT NULL,
    P_id  VARCHAR2(15)    NOT NULL,
    Doc_id  VARCHAR2(15)    NOT NULL,
    PRIMARY KEY (Res_id)
);
CREATE TABLE PRESCRIPTION(
    Pre_id      VARCHAR2(15) NOT NULL,
    P_id      VARCHAR2(15) NOT NULL,
    Doc_id      VARCHAR2(15) NOT NULL,
    Contents    VARCHAR2(200),
    PRIMARY KEY (Pre_id)
);
CREATE TABLE DOCTOR_SCHEDULE(
    S_id      VARCHAR2(15) NOT NULL,
    Doc_id    VARCHAR2(15) NOT NULL,
    S_Date    Date,
    S_Hour    NUMBER(3),
    S_Minute  NUMBER(3),
    S_State   NUMBER(3) DEFAULT 0 NOT NULL,
    PRIMARY KEY (S_id)
);

CREATE TABLE DEPARTMENT
(
   Dep_id   VARCHAR2(15)   NOT NULL,
   Dep_name   VARCHAR2(30)   NOT NULL,
   Hos_id VARCHAR(15) NOT NULL,
   PRIMARY KEY (Dep_id)
);

CREATE TABLE DOCTOR
(
   Doc_id   VARCHAR2(15)   NOT NULL,
   Doc_password   VARCHAR2(15)   NOT NULL,
   Phone_number   VARCHAR2(20),
   Doc_name   VARCHAR2(30)   NOT NULL,
   Dep_id   VARCHAR2(15)   NOT NULL,
   PRIMARY KEY (Doc_id)
);

CREATE TABLE PARMACY
(
   Par_id   VARCHAR2(15)   NOT NULL,
   Par_location   VARCHAR2(200)   NOT NULL,
   Par_name   VARCHAR2(100)   NOT NULL,
   PRIMARY KEY (Par_id) 
);

